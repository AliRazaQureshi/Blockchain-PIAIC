var num_1 = parseInt(prompt("Enter first number: "));
var num_2 = parseInt(prompt("Enter second number: "));
var sub = num_1 - num_2;
var mul = num_1 * num_2
var mod = num_1 % num_2;
document.writeln("Substraction of "+num_1+" and "+num_2+" is "+sub);
document.writeln("Multiplication of "+num_1+" and "+num_2+" is "+mul);
document.writeln("Modulus of "+num_1+" and "+num_2+" is "+mod);